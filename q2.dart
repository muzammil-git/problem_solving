//   Q.2: Take two variables and store age
//   then using if/else condition to determine
//   oldest and youngest among them.

void main() {
  int johnsAge = 35;
  int martinAge = 30;

  if (johnsAge > martinAge) {
    print("John is Older and Martin is younger.");
  } else if (martinAge < johnsAge) {
    print("Martin is Older and John is younger.");
  }
}
