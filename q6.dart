// Q.6: Write a program to check whether an alphabet is a vowel or consonant.

void main() {
  String alphabet = "e";

  if (alphabet == "a" ||
      alphabet == "e" ||
      alphabet == "i" ||
      alphabet == "o" ||
      alphabet == "u") {
    print("Alphabet '$alphabet' is a vowel");
  } else {
    print("Alphabet '$alphabet' is a consonant");
  }
}
