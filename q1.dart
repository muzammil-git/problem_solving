//   and assign values then
//   check if they are square values or rectangle values.
//   i.e: if both values are equal then it's square otherwise rectangle.

void main() {
  int length = 10;
  int breadth = 10;

  if (length == breadth) {
    print("Its a Square");
  } else {
    print("Its a Rectangle");
  }
}
